const mongoose = require("mongoose");

const branchSchema = mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  image: {
    type: String,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  code: {
    type: String,
    required: true,
  },
});

const Branch = mongoose.model("Branch", branchSchema);

module.exports = Branch;
